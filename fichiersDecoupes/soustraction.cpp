//--------------------------Soustraction
/// \brief Fonction de soustraction de deux nombre de type int32_t
///
///
/// \param operandeA est un entier sur 32 bits
/// \param operandeB est un entier sur 32 bits
/// \return La soustraction des deux entiers 32 bits saisie par l'utilisateur.
/// \sa soustraction(int64_t, int64_t), soustraction(float, float), soustraction(double, double)
///
int32_t soustraction(int32_t operandeA, int32_t operandeB)
{
	return operandeA - operandeB;
}

/// \brief Fonction de soustraction de deux nombre de type int64_t
///
///
/// \param operandeA est un entier sur 64 bits
/// \param operandeB est un entier sur 64 bits
/// \return La soustraction des deux entiers 64 bits saisie par l'utilisateur.
/// \sa soustraction(float, float), soustraction(double, double), soustraction(int32_t, int32_t)
///
int64_t soustraction(int64_t operandeA, int64_t operandeB)
{
	return operandeA - operandeB;
}
	
