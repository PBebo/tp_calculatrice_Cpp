///
float soustraction(float operandeA, float operandeB)
{
	return operandeA - operandeB;
}
	
/// \brief Fonction de soustraction de deux nombre de type Double
///
///
/// \param operandeA est un Double
/// \param operandeB est un Double
/// \return La soustraction des deux Double saisie par l'utilisateur.
