/// \brief Fonction d'addition de deux nombre de type int32_t
///
///
/// \param operandeA est un entier sur 32 bits
/// \param operandeB est un entier sur 32 bits
/// \return La somme des deux entiers 32 bits saisie par l'utilisateur.
/// \sa ajouter(int64_t, int64_t), ajouter(float, float), ajouter(double, double)
///
int32_t ajouter(int32_t operandeA, int32_t operandeB)
{
	return operandeA + operandeB;
}

