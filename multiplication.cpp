//--------------------------Multiplication
/// \brief Fonction de soustraction de deux nombre de type int32_t
///
///
/// \param operandeA est un entier sur 32 bits
/// \param operandeB est un entier sur 32 bits
/// \return La multiplication des deux entiers 32 bits saisie par l'utilisateur.
/// \sa multiplication(int64_t, int64_t), multiplication(float, float), multiplication(double, double)
///
int32_t multiplication(int32_t operandeA, int32_t operandeB)
{
	return operandeA * operandeB;
}

/// \brief Fonction de multiplication de deux nombre de type int64_t
///
///
/// \param operandeA est un entier sur 64 bits
/// \param operandeB est un entier sur 64 bits
/// \return La multiplication des deux entiers 64 bits saisie par l'utilisateur.
/// \sa multiplication(float, float), multiplication(double, double), multiplication(int32_t, int32_t)
///
int64_t multiplication(int64_t operandeA, int64_t operandeB)
{
	return operandeA * operandeB;
}
	
/// \brief Fonction de multiplication de deux nombre de type Float
///
///
/// \param operandeA est un Float
/// \param operandeB est un Float
/// \return La multiplication des deux Float saisie par l'utilisateur.
/// \sa multiplication(double, double), multiplication(int32_t, int32_t), multiplication(int64_t, int64_t)
///
float multiplication(float operandeA, float operandeB)
{
	return operandeA * operandeB;
}
	
/// \brief Fonction de multiplication de deux nombre de type Double
///
///
/// \param operandeA est un Double
/// \param operandeB est un Double
/// \return La multiplication des deux Double saisie par l'utilisateur.
/// \sa multiplication(int32_t, int32_t), multiplication(int64_t, int64_t), multiplication(float, float)
///	
double multiplication(double operandeA, double operandeB)
{
	return operandeA * operandeB;
}
